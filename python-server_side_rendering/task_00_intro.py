def generate_invitations(template, attendees):
    if not isinstance(template, str) or not isinstance(attendees, list):
        print("Invalid input type")
        return

    if not all(isinstance(a, dict) for a in attendees):
        print("Invalid input type")
        return

    if template.strip() == "":
        print("Template is empty, no output files generated.")
        return

    if len(attendees) == 0:
        print("No data provided, no output files generated.")
        return

    for index, attendee in enumerate(attendees, start=1):
        content = template

        value = attendee.get("name") or "N/A"
        content = content.replace("{name}", value)

        value = attendee.get("event_title") or "N/A"
        content = content.replace("{event_title}", value)

        value = attendee.get("event_date") or "N/A"
        content = content.replace("{event_date}", value)

        value = attendee.get("event_location") or "N/A"
        content = content.replace("{event_location}", value)

        filename = f"output_{index}.txt"
        with open(filename, "w") as f:
            f.write(content)